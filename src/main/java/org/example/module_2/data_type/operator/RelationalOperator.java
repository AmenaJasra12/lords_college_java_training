package lords_college_java_training.src.main.java.org.example.module_2.data_type.operator;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 20;
        int b = 20;
        System.out.println(a +" > "+ b+":" + (a > b));
        System.out.println(a +" < "+ b+":" + (a < b));
        System.out.println(a +" >= "+ b+":" + (a >= b));
        System.out.println(a +" <= "+ b+":" + (a <= b));
        System.out.println(a +" == "+ b+":" + (a == b));
    }
}
