package org.example.module_2.operator;

public class LogicalOperator {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = true;

        // Logical AND operator (&&)
        System.out.println("x && y: " + (x && y));

        // Logical OR operator (||)
        System.out.println("x || y: " + (x || y));

        // Logical NOT operator (!)
        System.out.println("!x: " + (!x));
    }
}
