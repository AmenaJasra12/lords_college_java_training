package lords_college_java_training.src.main.java.org.example.module_2.operator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("a: "+a);
        a += 10;
        System.out.println(a);
        a -= 8;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 3;
        System.out.println(a);
    }
}